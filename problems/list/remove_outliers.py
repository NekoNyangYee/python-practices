# 🧩 문제: 이상치 제거하기 (Remove Outliers)

# 정수 리스트가 주어졌을 때,
# 리스트에서 최댓값과 최솟값을 각각 하나씩 제거한 새로운 리스트를 만들어 출력하세요.

# 입력 예시 1
# [10, 3, 5, 9, 2, 8]

# 출력 예시 1
# [3, 5, 9, 8]


# 이유:
# 최솟값 2 제거, 최댓값 10 제거 → [3, 5, 9, 8]

# 입력 예시 2
# [1, 1, 2, 3, 10, 10]

# 출력 예시 2
# [1, 2, 3, 10]


# 이유:
# 최솟값(1) 하나, 최댓값(10) 하나만 제거 → 중복이 남아도 됨.

def remove_outliers(input):
    input.remove(min(input))
    input.remove(max(input))
    print(input)

remove_outliers([1, 1, 2, 3, 10, 10])

